using System;
using System.Collections.Generic;

namespace work_for_final
{
    internal class Program
    {
        static void Main(string[] args)
        {
            List<Product> products = new List<Product>
            {
                new Electronics("Laptop", 1500m, "Electronics", 24),
                new Food("Apple", 1m, "Food", DateTime.Now.AddDays(-1), DateTime.Now.AddDays(6)),
                new Electronics("Smartphone", 800m, "Electronics", 12),
                new Food("Milk", 2m, "Food", DateTime.Now.AddDays(-3), DateTime.Now.AddDays(4))
            };

            Console.WriteLine("ყველა პროდუქტის ინფორმაცია:");
            foreach (var product in products)
            {
                product.PrintDetails();
                Console.WriteLine();
            }

            Console.WriteLine("ფასდაკლებით პროდუქტები (10%):");
            foreach (var product in products)
            {
                decimal discountedPrice = product.CalculateDiscount(10);
                Console.WriteLine($"{product.Name}: ფასი {product.Price}, ფასდაკლებით {discountedPrice}");
            }
        }
    }
}


